# COnverting upper string to lower and vice versa

def swap_case(s):
    f=''
    for char in s:
        if char.isupper():
            f+=char.lower()
        else:
            f+=char.upper()
    return f

if __name__ == '__main__':
    s = input()
    result = swap_case(s)
    print(result)
    
